class Hinhchunhat:
    def __init__(self, length, width):
        self.length = length
        self.width = width

    def area(self):
        return self.length * self.width
rectangle = Hinhchunhat(4, 9)
print("Diện tích hình chữ nhật:", rectangle.area())
